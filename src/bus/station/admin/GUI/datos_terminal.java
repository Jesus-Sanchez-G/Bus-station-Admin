    private void modificar_Terminal_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificar_Terminal_ButtonActionPerformed
        // TODO add your handling code here:
         bus.station.admin.GUI.FORMS.ConexionPostgres c = new bus.station.admin.GUI.FORMS.ConexionPostgres();
        c.Conectar();
        if(!DATOS[1].equalsIgnoreCase(this.jTextField1.getText())||!DATOS[2].equalsIgnoreCase(this.jTextField2.getText())
           ||!DATOS[3].equalsIgnoreCase(this.jTextField3.getText())||!DATOS[4].equalsIgnoreCase(this.jTextField4.getText()))
        {
            
            String botones[]={"Si modificar","Cancelar"};
            int variable = JOptionPane.showOptionDialog (null, " Â¿Seguro que quieres modificar los datos de la terminal?", "Cita", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null/*icono*/, botones, botones[0]);
            if(variable==0)
            c.modificarTerminal(Integer.parseInt(DATOS[0]),this.jTextField1.getText() , Integer.parseInt(this.jTextField2.getText()), Long.parseLong(this.jTextField3.getText()),Integer.parseInt(this.jTextField4.getText()));    
            else
                JOptionPane.showMessageDialog(null, "Sin modificaciones");
        }else
                JOptionPane.showMessageDialog(null, "No hay datos modificados");
                      
        
    }//GEN-LAST:event_modificar_Terminal_ButtonActionPerformed

